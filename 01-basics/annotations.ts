/**
 * Annotations
 * 
 * São usadas para especificar o tipo de dado de variáveis
 * parâmetros, retorno de funções, e outros tipos de valores.
 * 
 * Ajudam os desenvolvedores a encontrar erros mais facilmente no desenvolvimento
 * permitindo que eles especifiquem que tipos de valores podem ser atribuídos a
 * uma variável ou passados como argumentos para uma função.
*/

// let myVar: type = value;
let myName: string = "John";
myName = "Outro nome";
console.log(myName);

// ERRO
// myName = 12;

/**
 * Types:
 * 
 * string ("", '')
 * number (1, 2, 3, ...)
 * boolean (true/false)
 */